import React from "react";
import { Product } from "../../types";

interface AdminDashboardProps {
  products: Product[];
}

export const AdminDashboard: React.FC<AdminDashboardProps> = ({ products }) => {
  // Stats Calculation
  const totalProducts = products.length;
  const categories = products.reduce(
    (acc, p) => {
      acc[p.category] = (acc[p.category] || 0) + 1;
      return acc;
    },
    {} as Record<string, number>,
  );

  const categoryData = Object.entries(categories).map(
    ([name, value], index) => {
      const percentage = (value / totalProducts) * 100;
      // Calculate cumulative offset for pie slices (simplified as conic gradient stops)
      return { name, value, percentage };
    },
  );

  // Simple Conic Gradient For Pie Chart
  let currentAngle = 0;
  const pieSegments = categoryData.map((cat, i) => {
    const start = currentAngle;
    const end = currentAngle + cat.percentage * 3.6; // 360 deg / 100
    currentAngle = end;

    // Aesthetic colors fitting the theme
    const colors = ["#333", "#666", "#999", "#CCC", "#EEE"];
    const color = colors[i % colors.length];

    return `${color} ${start}deg ${end}deg`;
  });

  const pieGradient = `conic-gradient(${pieSegments.join(", ")})`;

  return (
    <div className="space-y-16 animate-in slide-in-from-bottom-4 duration-700">
      <div className="flex justify-between items-end border-b border-black/5 dark:border-white/5 pb-8">
        <h2 className="text-4xl md:text-6xl font-thin tracking-tighter">
          SISTEMA <br />
          <span className="opacity-40 italic">MÉTRICO.</span>
        </h2>
        <span className="font-futuristic text-[10px] tracking-widest text-neutral-500">
          SYSTEM_UPTIME: 99.9%
        </span>
      </div>

      {/* KPI Cards */}
      <div className="grid grid-cols-1 md:grid-cols-4 gap-4 md:gap-8">
        {[
          { label: "TOTAL_SKU", value: totalProducts, change: "ACTIVE" },
          { label: "VALOR_STOCK", value: "$42k", change: "+5%" },
          { label: "QUERIES_AI", value: "2,481", change: "+24%" },
          { label: "CONVERSION", value: "4.2%", change: "-2%" },
        ].map((stat, i) => (
          <div
            key={i}
            className="p-8 border border-black/5 dark:border-white/5 bg-neutral-50 dark:bg-black/40 hover:border-black/20 dark:hover:border-white/20 transition-colors"
          >
            <span className="font-futuristic text-[9px] tracking-widest text-neutral-600 block mb-4 uppercase">
              {stat.label}
            </span>
            <div className="flex items-baseline gap-4">
              <span className="text-3xl font-light tracking-tighter">
                {stat.value}
              </span>
              <span
                className={`text-[9px] font-futuristic tracking-widest ${
                  stat.change === "ACTIVE"
                    ? "text-blue-500"
                    : stat.change.startsWith("+")
                      ? "text-green-500"
                      : "text-red-500"
                }`}
              >
                {stat.change}
              </span>
            </div>
          </div>
        ))}
      </div>

      {/* Charts Section */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8 md:gap-16">
        {/* Pie Chart Card */}
        <div className="p-8 md:p-12 border border-black/5 dark:border-white/5 bg-neutral-50 dark:bg-black/20">
          <h3 className="font-futuristic text-[10px] tracking-[0.3em] uppercase mb-12 dark:text-white text-black">
            DISTRIBUCIÓN_CATEGORÍA
          </h3>
          <div className="flex flex-col md:flex-row items-center gap-12">
            <div
              className="relative w-48 h-48 rounded-full shadow-2xl"
              style={{ background: pieGradient }}
            >
              <div className="absolute inset-4 rounded-full bg-neutral-50 dark:bg-[#111] flex items-center justify-center">
                <span className="font-futuristic text-2xl tracking-tighter opacity-50">
                  {totalProducts}
                </span>
              </div>
            </div>

            <div className="space-y-4 flex-1 w-full">
              {categoryData.map((cat, i) => (
                <div
                  key={i}
                  className="flex items-center justify-between group"
                >
                  <div className="flex items-center gap-3">
                    <div
                      className="w-2 h-2 rounded-full"
                      style={{
                        backgroundColor: [
                          "#333",
                          "#666",
                          "#999",
                          "#CCC",
                          "#EEE",
                        ][i % 5],
                      }}
                    ></div>
                    <span className="text-xs font-futuristic uppercase tracking-wider text-neutral-500 group-hover:text-black dark:group-hover:text-white transition-colors">
                      {cat.name}
                    </span>
                  </div>
                  <span className="text-xs font-mono opacity-50">
                    {Math.round(cat.percentage)}%
                  </span>
                </div>
              ))}
            </div>
          </div>
        </div>

        {/* Bar Chart Mockup (CSS only) */}
        <div className="p-8 md:p-12 border border-black/5 dark:border-white/5 bg-neutral-50 dark:bg-black/20 flex flex-col">
          <h3 className="font-futuristic text-[10px] tracking-[0.3em] uppercase mb-12 dark:text-white text-black">
            RENDIMIENTO_MENSUAL
          </h3>
          <div className="flex-1 flex items-end justify-between gap-4 h-48 w-full border-b border-black/10 dark:border-white/10 pb-4">
            {[40, 65, 30, 85, 50, 75, 90, 60, 45, 80, 55, 70].map((h, i) => (
              <div
                key={i}
                className="relative group flex-1 bg-neutral-200 dark:bg-neutral-800 rounded-t hover:bg-black dark:hover:bg-white transition-all duration-500"
                style={{ height: `${h}%` }}
              >
                <div className="absolute -top-8 left-1/2 -translate-x-1/2 opacity-0 group-hover:opacity-100 transition-opacity bg-black dark:bg-white text-white dark:text-black text-[9px] py-1 px-2 rounded">
                  {h}%
                </div>
              </div>
            ))}
          </div>
          <div className="flex justify-between mt-4 text-[9px] font-futuristic text-neutral-400">
            <span>ENE</span>
            <span>FEB</span>
            <span>MAR</span>
            <span>ABR</span>
            <span>MAY</span>
            <span>JUN</span>
            <span className="hidden md:inline">JUL</span>
            <span className="hidden md:inline">AGO</span>
            <span className="hidden md:inline">SEP</span>
            <span className="hidden md:inline">OCT</span>
            <span className="hidden md:inline">NOV</span>
            <span className="hidden md:inline">DIC</span>
          </div>
        </div>
      </div>
    </div>
  );
};
